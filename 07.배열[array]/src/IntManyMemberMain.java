
public class IntManyMemberMain {
//배열하는거
	public static void main(String[] args) {
		//배열은 타입이 존재 안함, [] 찍으면 배열임
		IntManyMember intManyMember = new IntManyMember();
		
		
		intManyMember.i0=90;
		intManyMember.i1=45;
		intManyMember.i2=77;
		intManyMember.i3=89;
		intManyMember.i4=99;
		
		
		System.out.println(intManyMember.i0);
		System.out.println(intManyMember.i1);
		System.out.println(intManyMember.i2);
		System.out.println(intManyMember.i3);
		System.out.println(intManyMember.i4);
		
		
	}

}
